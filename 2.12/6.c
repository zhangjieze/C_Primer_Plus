//定义int型变量toes ,输出toes和toes的平方，和toes的立方
#include <stdio.h>
int main(void){
    int toes=10; // 定义int型变量toes并赋值为10
    printf("toes = %d\n", toes); // 输出toes的值
    printf("toes squared = %d\n", toes * toes); // 输出toes的平方
    printf("toes cubed = %d\n", toes * toes * toes); // 输出toes的立方
    return 0; 
}